@st.cache_data
def calc_feature_importance(routes_df):
    np.random.seed(42)
    features = {
        "Travel Time":    "travel_time_hr",
        "Fuel Cost":      "fuel_cost",
        "Toll Cost":      "toll_cost",
        "Driver Cost":    "driver_cost",
        "Carbon Emitted": "carbon_kg",
        "SLA Breach":     "sla_breach_hr",
        "Package Weight": "weight",
    }
    X = routes_df[list(features.values())].copy()
    y = routes_df["mo_score"].values
    base_mae = np.mean(np.abs(y - y.mean()))
    scores = {}
    for label, col in features.items():
        Xs = X.copy(); Xs[col] = np.random.permutation(Xs[col].values)
        proxy = Xs.apply(lambda c: (c - c.mean()) / (c.std() + 1e-9)).mean(axis=1)
        scores[label] = abs(np.mean(np.abs(y - proxy.values)) - base_mae)
    total = sum(scores.values()) + 1e-9
    return {k: round(v/total*100, 1) for k, v in sorted(scores.items(), key=lambda x: -x[1])}


@st.cache_data
def calc_stop_contributions(routes_df):
    cols    = ["travel_time_hr","fuel_cost","toll_cost","driver_cost","carbon_kg","sla_breach_hr"]
    labels  = ["Travel Time","Fuel Cost","Toll Cost","Driver Cost","Carbon","SLA Breach"]
    wts     = [0.30, 0.20, 0.05, 0.15, 0.20, 0.10]
    df = routes_df[cols].copy()
    for c in cols:
        rng = df[c].max() - df[c].min()
        df[c] = (df[c] - df[c].min()) / (rng + 1e-9)
    for i, c in enumerate(cols):
        df[c] = df[c] * wts[i]
    df.columns = labels
    df["city"]     = routes_df["city"].values
    df["vehicle"]  = routes_df["vehicle"].values
    df["mo_score"] = routes_df["mo_score"].values
    return df


