# ── Simulation helpers ────────────────────────────────────────────────────────
def sim_traffic(city, new_mult, ships_df, routes_df):
    row       = ships_df[ships_df["city"] == city].iloc[0]
    orig_mult = float(row["traffic_mult"])
    d         = haversine(DEPOT["latitude"], DEPOT["longitude"], float(row["latitude"]), float(row["longitude"]))
    orig_t    = d / (AVG_SPEED / orig_mult)
    new_t     = d / (AVG_SPEED / new_mult)
    pct       = (new_t - orig_t) / orig_t * 100
    res       = {"city": city, "orig_mult": orig_mult, "new_mult": new_mult,
                 "pct": round(pct, 1), "triggered": pct > 30, "dist_km": round(d, 1)}
    if res["triggered"]:
        aff = routes_df[routes_df["city"] == city]["vehicle"].values
        if len(aff):
            vid = int(aff[0])
            orig = routes_df[routes_df["vehicle"] == vid].sort_values("stop_order").copy()
            mask = orig["city"] == city
            reopt = pd.concat([orig[~mask], orig[mask]]).reset_index(drop=True)
            def rdist(df):
                pts = [DEPOT] + df.to_dict("records") + [DEPOT]
                return round(sum(haversine(pts[i]["latitude"],pts[i]["longitude"],pts[i+1]["latitude"],pts[i+1]["longitude"]) for i in range(len(pts)-1)), 1)
            res.update({"vid": vid, "orig": orig, "reopt": reopt,
                        "od": rdist(orig), "nd": rdist(reopt)})
    return res


def sim_escalate(city, ships_df, routes_df):
    row  = ships_df[ships_df["city"] == city].iloc[0]
    op   = str(row["priority"]); os = int(row["sla_hours"])
    if op == "HIGH":
        return {"already": True, "city": city}
    aff  = routes_df[routes_df["city"] == city]["vehicle"].values
    vid  = int(aff[0]) if len(aff) else 1
    orig = routes_df[routes_df["vehicle"] == vid].sort_values("stop_order").copy()
    mask = orig["city"] == city
    new  = pd.concat([orig[mask], orig[~mask]]).reset_index(drop=True)
    ot   = round(float(orig["travel_time_hr"].sum()), 1)
    return {"already": False, "city": city, "op": op, "os": os,
            "vid": vid, "orig": orig, "new": new, "ot": ot, "nt": round(ot*0.88, 1)}


